def solution(n):
    # 1칸, 2칸일 때는 규칙 없이 바로 정답을 반환합니다.
    if n == 1:
        return 1
    if n == 2:
        return 2
    
    # n = n-2 + n-1
    # a는 n-2번째 값, b는 n-1번째 값을 의미합니다.
    a, b = 1, 2
    
    # 3번째 칸부터 n번째 칸까지 피보나치 규칙을 적용해 앞으로 나아갑니다.
    for _ in range(3, n + 1):
        # 새로운 b(현재 칸)는 이전 두 값(a + b)을 더한 것이고, 
        # a는 방금 전의 b 값으로 갱신됩니다.
        a, b = b, (a + b) % 1234567
        
    return b